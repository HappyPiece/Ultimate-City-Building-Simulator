            AmogusVariants = new Dictionary<string, string>();
            InitializeVariants();
        }

        private int HashSeed(string seed)
        {
            MD5 hasher = MD5.Create();
            var hashed = hasher.ComputeHash(Encoding.UTF8.GetBytes(seed));
            return BitConverter.ToInt32(hashed, 0);
        }
