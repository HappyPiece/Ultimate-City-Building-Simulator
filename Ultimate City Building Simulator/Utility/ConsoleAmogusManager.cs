        public override string RetrieveAmogus(string seed)
        {
            if (seed == null || AmogusVariants.Count <= 0) return "";
            if (AmogusVariants.ContainsKey(seed))
            {
                return AmogusVariants[seed];
            }

            var index = Math.Abs(HashSeed(seed) % AmogusVariants.Count);
            return AmogusVariants.Values.ToArray()[index];
        }
        public ConsoleAmogusManager()
